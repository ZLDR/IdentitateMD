#!/usr/bin/env node

/**
 * Generează institutions-data.js pentru Logo Editor
 * Citește toate JSON-urile din website/src/data/institutions/
 * și SVG-urile din packages/logos/logos/ sau website/public/logos/
 * Le inline-uiește într-un fișier JS importabil din HTML.
 * 
 * Rulează: node tools/generate-editor-data.js
 */

const { readFileSync, writeFileSync, readdirSync, existsSync } = require('fs');
const { join } = require('path');

const INSTITUTIONS_DIR = join(__dirname, '../website/src/data/institutions');
const LOGOS_DIRS = [
  join(__dirname, '../website/public/logos'),
  join(__dirname, '../packages/logos/logos'),
];
const OUTPUT_FILE = join(__dirname, 'institutions-data.js');

/**
 * Citește un SVG și îl transformă în data URI
 */
function svgToDataUri(svgPath) {
  try {
    const svg = readFileSync(svgPath, 'utf8').trim();
    const encoded = svg
      .replace(/\n/g, ' ')
      .replace(/\s+/g, ' ')
      .replace(/#/g, '%23')
      .replace(/"/g, "'");
    return `data:image/svg+xml,${encodeURIComponent(svg)}`;
  } catch (e) {
    return null;
  }
}

/**
 * Rezolvă o cale de asset (ex: /logos/ro-anaf/horizontal/color.svg) 
 * în path-uri absolute pe disc
 */
function resolveAssetPath(assetPath) {
  if (!assetPath) return null;
  // assetPath e ceva gen "/logos/ro-anaf/horizontal/color.svg"
  const relative = assetPath.replace(/^\//, '');
  for (const base of LOGOS_DIRS) {
    // base e website/public/logos, assetPath e logos/ro-anaf/...
    // trebuie să scoatem "logos/" din relative
    const withoutLogos = relative.replace(/^logos\//, '');
    const fullPath = join(base, withoutLogos);
    if (existsSync(fullPath)) return fullPath;
    // Încearcă și cu path-ul complet
    const fullPath2 = join(base, '..', relative);
    if (existsSync(fullPath2)) return fullPath2;
  }
  return null;
}

/**
 * Colectează toate SVG-urile dintr-o instituție și le inline-uiește
 */
function collectSvgs(inst) {
  const svgMap = {};
  if (!inst.assets) return svgMap;

  const layouts = ['horizontal', 'vertical', 'symbol'];
  const variants = ['color', 'dark_mode', 'white', 'black', 'monochrome'];

  for (const layout of layouts) {
    const group = inst.assets[layout];
    if (!group || typeof group !== 'object') continue;
    for (const variant of variants) {
      const assetPath = group[variant];
      if (!assetPath) continue;
      const path = typeof assetPath === 'string' ? assetPath : assetPath.local;
      if (!path) continue;
      const diskPath = resolveAssetPath(path);
      if (diskPath) {
        const dataUri = svgToDataUri(diskPath);
        if (dataUri) {
          svgMap[path] = dataUri;
        }
      }
    }
  }

  // Also handle main
  if (inst.assets.main) {
    for (const variant of variants) {
      const assetPath = inst.assets.main[variant];
      if (!assetPath) continue;
      const path = typeof assetPath === 'string' ? assetPath : assetPath.local;
      if (!path || svgMap[path]) continue;
      const diskPath = resolveAssetPath(path);
      if (diskPath) {
        const dataUri = svgToDataUri(diskPath);
        if (dataUri) {
          svgMap[path] = dataUri;
        }
      }
    }
  }

  return svgMap;
}

function generate() {
  const files = readdirSync(INSTITUTIONS_DIR)
    .filter(f => f.endsWith('.json') && !f.endsWith('.bak'));

  const institutions = [];
  const allSvgs = {};

  for (const file of files) {
    try {
      const raw = readFileSync(join(INSTITUTIONS_DIR, file), 'utf8');
      const data = JSON.parse(raw);
      if (data.id) {
        institutions.push(data);
        // Colectează SVG-urile
        const svgs = collectSvgs(data);
        Object.assign(allSvgs, svgs);
      }
    } catch (e) {
      console.warn(`⚠️  Skip ${file}: ${e.message}`);
    }
  }

  institutions.sort((a, b) => a.id.localeCompare(b.id));

  const svgCount = Object.keys(allSvgs).length;

  const output = `// Auto-generated by tools/generate-editor-data.js
// Rulează: node tools/generate-editor-data.js
// Data: ${new Date().toISOString()}
// Total: ${institutions.length} instituții, ${svgCount} SVG-uri inline

const INSTITUTIONS_DATA = ${JSON.stringify(institutions, null, 2)};

const SVG_DATA = ${JSON.stringify(allSvgs)};
`;

  writeFileSync(OUTPUT_FILE, output, 'utf8');
  console.log(`✅ Generated ${OUTPUT_FILE}`);
  console.log(`   ${institutions.length} instituții, ${svgCount} SVG-uri inline`);
}

generate();
